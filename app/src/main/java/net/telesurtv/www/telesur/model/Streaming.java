public class Streaming {

    private String name;
    private String sinopsis;
    private String photo;
    private String url;
    private String starHour;
    private String finishHour;
    private String starHourVenezuela;
    private String finishHourVenezuela;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSinopsis() {
        return sinopsis;
    }

    public void setSinopsis(String sinopsis) {
        this.sinopsis = sinopsis;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getStarHour() {
        return starHour;
    }

    public void setStarHour(String starHour) {
        this.starHour = starHour;
    }

    public String getFinishHour() {
        return finishHour;
    }

    public void setFinishHour(String finishHour) {
        this.finishHour = finishHour;
    }

    public String getStarHourVenezuela() {
        return starHourVenezuela;
    }

    public void setStarHourVenezuela(String starHourVenezuela) {
        this.starHourVenezuela = starHourVenezuela;
    }

    public String getFinishHourVenezuela() {
        return finishHourVenezuela;
    }

    public void setFinishHourVenezuela(String finishHourVenezuela) {
        this.finishHourVenezuela = finishHourVenezuela;
    }


}
